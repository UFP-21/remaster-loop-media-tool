from __future__ import annotations

import streamlit as st


def _card(title: str, bullets: list[str]) -> None:
    with st.container(border=True):
        st.subheader(title)
        for b in bullets:
            st.markdown(f"- {b}")


def page_home(state=None) -> None:
    st.title("Remaster+Loop")
    st.caption(
        "Приложение для подготовки аудио и видео: мастеринг, обрезка, авто-срез тишины, "
        "склейка треков, склейка видео, создание клипа и лупинг."
    )

    st.divider()

    st.header("🚀 Быстрый старт")

    _card(
        "1) 🎧 Мастеринг → улучшить трек",
        [
            "Открой **🎧 Мастеринг**.",
            "Загрузи 1 или несколько аудиофайлов.",
            "Выбери трек → послушай **Оригинал**.",
            "При необходимости сначала сделай **✂️ Обрезку** или **Авто-срез тишины**.",
            "Выбери пресет → сделай **превью 5–10 сек** → затем мастеринг **весь трек**.",
            "Если интро стало слишком громким — **выключи Loudnorm**.",
            "Если мастер сделал без Loudnorm — можно потом сделать **пост-нормализацию** готового файла.",
        ],
    )

    _card(
        "2) 🧩 Склейка треков → собрать микс/плейлист",
        [
            "Открой **🧩 Склейка треков**.",
            "Загрузи минимум 2 трека и расставь порядок (↑ ↓).",
            "Выбери режим: **Кроссфейд** или **Таймлайн**.",
            "Если слышишь «ступеньку» громкости на стыках — **не включай нормализацию каждого трека**.",
            "После склейки можно сделать **пост-нормализацию финального файла**.",
            "Можно экспортировать результат в **WAV** и **MP3 320k**.",
        ],
    )

    _card(
        "3) 🎞️ Склейка видео → один фон из нескольких клипов",
        [
            "Открой **🎞️ Склейка видео**.",
            "Загрузи клипы → расставь порядок.",
            "При необходимости задай:",
            "• **Trim start** — сколько секунд отрезать в начале",
            "• **Trim end** — сколько секунд отрезать в конце",
            "Нажми **Склеить видео** — получишь один MP4-фон.",
        ],
    )

    _card(
        "4) 🎬 Создать видеоклип → финальный MP4 (видео + аудио)",
        [
            "Открой **🎬 Создать видеоклип**.",
            "Выбери аудио (оригинал / после мастеринга / после склейки / после лупинга).",
            "Выбери фон: **видео** или **картинка**.",
            "Сделай 1 размер или несколько размеров.",
            "Готовый MP4 появится в `outputs/mp4/`.",
        ],
    )

    _card(
        "5) 🔁 Зацикливание → длинный луп под YouTube / видео",
        [
            "Открой **🔁 Зацикливание**.",
            "Выбери режим: по количеству повторов, по точной длительности или под длину видео.",
            "При необходимости включи **fade-out в конце**.",
            "Можно сразу подготовить результат для вкладки **🎬 Создать видеоклип**.",
        ],
    )

    st.divider()

    st.header("📁 Где лежат файлы")

    with st.container(border=True):
        st.markdown(
            """
- **uploads_audio/** — загруженные аудиофайлы  
- **uploads_video/** — загруженные видеофайлы  
- **uploads_images/** — загруженные картинки  
- **outputs/** — все готовые результаты  
- **previews/** — превью и временные мини-файлы  
- **session_*** — временные папки, их можно удалять через вкладку очистки
"""
        )
        st.info(
            "Если результат уже виден в приложении, значит файл уже лежит в Work-папке. "
            "Его можно забрать прямо оттуда, без скачивания через браузер."
        )

    st.divider()

    st.header("💡 Практические советы")

    with st.container(border=True):
        st.markdown(
            """
- Для **мастеринга** сначала делай короткое **превью**, потом уже весь трек.
- Если после мастеринга **интро стало слишком громким** — отключи **Loudnorm**.
- Если в **склейке** слышна **ступенька** на входе следующего трека — выключи нормализацию каждого трека и нормализуй уже **готовый результат**.
- Для больших списков треков используй **батчи**.
- После важной операции удобно сразу нажимать **«Открыть папку»** и проверять результат.
"""
        )

    st.divider()

    st.success("Начать лучше всего с вкладки **🎧 Мастеринг** или **🧩 Склейка треков** — в зависимости от задачи.")


render = page_home