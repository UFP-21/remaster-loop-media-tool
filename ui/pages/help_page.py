GUIDE_MD = r"""
# 📘 Руководство Remaster+Loop

Remaster+Loop помогает подготовить аудио и видео для релиза, YouTube, фона или клипа.

Основные вкладки:

- 🎧 **Мастеринг**
- 🧩 **Склейка треков**
- 🎞️ **Склейка видео**
- 🎬 **Создать видеоклип**
- 🔁 **Зацикливание**
- 🧹 **Очистка Work-папки**

---

# 🧠 Где лежат ваши файлы

Приложение работает с **рабочей папкой (Work-папкой)**.  
В ней обычно есть:

- `uploads_audio/` — загруженные аудиофайлы
- `uploads_video/` — загруженные видеофайлы
- `uploads_images/` — загруженные картинки
- `outputs/` — готовые результаты
- `previews/` — превью и временные мини-файлы
- `session_*` — временные папки

## Как получить результат без скачивания из браузера
Если вы видите результат в приложении, значит файл уже лежит в Work-папке, обычно в `outputs/...`.

То есть можно:
- открыть папку результата,
- забрать файл прямо с диска,
- не пользоваться скачиванием из браузера.

---

# 🎧 Мастеринг

## Что умеет
Во вкладке мастеринга можно:

- загрузить один или несколько треков
- выбрать пресет
- сделать превью 5–10 секунд
- сделать мастеринг всего трека
- обрезать трек вручную
- автоматически срезать тишину
- сделать пакетный мастеринг
- сделать пост-нормализацию готового мастера
- экспортировать MP3 320k

## Как пользоваться (обычный сценарий)
1. Загрузите трек(и)
2. Выберите нужный трек
3. Послушайте **Оригинал**
4. При необходимости:
   - ✂️ **Обрежьте начало/конец**
   - 🤖 **Срежьте тишину**
5. Выберите пресет
6. Сделайте **превью**
7. Сделайте мастеринг всего трека
8. При необходимости:
   - отключите **Loudnorm**
   - или сделайте **пост-нормализацию** уже готового файла

## Loudnorm — когда включать, когда выключать
**Loudnorm** выравнивает громкость трека по LUFS.

Это полезно, если:
- исходник слишком тихий
- хочется получить более “ровкий” итоговый уровень

Но иногда Loudnorm может сделать **интро слишком громким**.  
Если вы слышите, что начало трека стало “выскакивать” — **выключите Loudnorm**.

### Практическое правило
- Если нужен быстрый “громче и ровнее” → Loudnorm **включён**
- Если важно сохранить естественную динамику интро/вступления → Loudnorm **выключен**
- Если мастер сделали без Loudnorm, но итог нужно выровнять → используйте **пост-нормализацию мастера**

## ✂️ Ручная обрезка
Ручная обрезка работает так:

- **Срезать в начале (сек)** — сколько секунд убрать с начала
- **Срезать в конце (сек)** — сколько секунд убрать с конца

Пример:  
трек 100 сек  
- срезать в начале = 2
- срезать в конце = 3

Останется отрезок:
**с 2-й по 97-ю секунду**

### Режимы края
- **Резко (cut)** — без плавности
- **Плавно (fade-out)** — с мягким затуханием в конце
- есть опциональный fade-in в начале

## 🤖 Авто-срез тишины
Автоматический режим пытается сам убрать тишину в начале и в конце.

Параметры:
- **Порог тишины (dB)**  
- **Минимальная тишина (сек)**  
- **Оставить запас (сек)**

Если режет слишком сильно — сделайте порог более “тихим”, например **-45 dB**.  
Если не режет почти ничего — поднимите порог, например **-30 dB**.

## Имена файлов мастеринга
Результаты обычно сохраняются в:
- `outputs/mastering/`
- `outputs/trim/`
- `outputs/mastering_previews/`

Имена делаются понятными:
- `Трек — Master.wav`
- `Трек — Trim.wav`
- `Трек — Trim (Fade).wav`
- `Трек — Master (Norm -14).wav`

Если файл уже существует, приложение добавляет:
- `(2)`, `(3)` и т.д.

---

# 🧩 Склейка треков

## Для чего
Собрать несколько треков в один длинный микс / сет / фон.

## Базовый сценарий
1. Загрузите минимум 2 трека
2. Расставьте порядок (↑ ↓)
3. Выберите режим
4. Нажмите **Склеить**
5. Получите результат в `outputs/concat/`

## Режимы
### 1) Кроссфейд
Обычная последовательная склейка:
- конец первого трека уходит
- начало второго появляется

### 2) Таймлайн MIX
Треки можно накладывать со сдвигом, как дорожки.

### 3) Таймлайн REPLACE
Один фрагмент заменяет другой с выбранной позиции.

## Важное про громкость
Во вкладке есть опция:
**“Выровнять громкость между треками”**

Она может быть полезна, но иногда вызывает
**“ступеньку” громкости** на старте следующего трека.

### Если слышна ступенька
Сделайте так:
1. **Выключите** нормализацию каждого трека
2. Склейте треки
3. Потом нажмите **пост-нормализацию результата**

Это лучший вариант, потому что:
- стыки остаются ровнее
- нет скачка громкости на начале следующего трека
- выравнивается уже весь финальный файл, а не куски по отдельности

## Пост-нормализация результата
После склейки можно:
- выровнять итоговую громкость по LUFS
- включить limiter как страховку от клипов
- экспортировать MP3 320k

---

# 🎞️ Склейка видео

## Для чего
Склеить несколько клипов в один фон или ролик.

## Как работает обрезка
Во вкладке видео:

- **Trim start** = сколько секунд отрезать в начале
- **Trim end** = сколько секунд отрезать в конце

Пример:  
клип 20 секунд  
- Trim start = 2
- Trim end = 3

Останется:
**с 2-й по 17-ю секунду**

Это удобно, потому что не нужно считать абсолютную конечную секунду.

## Где лежит результат
Обычно:
- `outputs/video_concat/`

---

# 🎬 Создать видеоклип

## Для чего
Собрать финальный MP4:
- аудио
- плюс фон-видео или картинка

## Базовый сценарий
1. Выберите аудио
2. Выберите фон (видео или картинка)
3. Сделайте 1 размер или несколько размеров
4. Получите результат в `outputs/mp4/`

Если раньше подготовили материал во вкладке **🔁 Зацикливание**, его можно быстро отправить сюда.

---

# 🔁 Зацикливание

## Для чего
Сделать длинный луп:
- по количеству повторов
- по точной длительности
- под длительность видео

## Что умеет
- кроссфейд между повторами
- fade-out в конце
- экспорт WAV/MP3
- подготовка пары аудио+видео для вкладки **🎬 Создать видеоклип**

---

# 🧹 Очистка Work-папки

## Что удаляется
Во вкладке очистки можно удалять:
- временные папки `session_*`
- мусор после прогонов
- накопленные временные файлы

## Что не удаляется
- сам код проекта
- ваши основные файлы проекта
- папки приложения, если вы явно не указали удалить результат

---

# ⚠️ Что делать, если что-то пошло не так

## 1) FFmpeg / ffprobe не найдены
Симптомы:
- не определяется длительность
- не работает обрезка
- не создаются аудио/видео результаты

Проверьте:
- установлен ли FFmpeg
- добавлен ли он в PATH
- или задан ли `FFMPEG_PATH` / `FFPROBE_PATH` в `.env`

## 2) Большие списки треков
Если треков очень много:
- используйте батчи
- не включайте лишнюю нормализацию на каждый трек
- сначала делайте результат, потом нормализуйте финал

## 3) Интро стало слишком громким после мастеринга
Решение:
- выключить **Loudnorm**
- или сделать сначала trim/auto-trim
- или мастерить без Loudnorm и нормализовать уже готовый финал

## 4) На склейке слышна ступенька
Решение:
- выключить “выровнять громкость между треками”
- после склейки сделать **пост-нормализацию результата**

---

# ✅ Короткая памятка

- Интро стало слишком громким → **выключи Loudnorm**
- На склейке слышна ступенька → **не нормализуй каждый трек, нормализуй финал**
- Нужно убрать тишину → **Авто-срез тишины**
- Нужно точно убрать край → **Ручная обрезка**
- Нужно просто забрать результат → открывай `outputs/...` в Work-папке
"""


def page_help(state: AppState) -> None:
    st.title("📘 Руководство")
    st.markdown(GUIDE_MD)


render = page_help